<h1>Objet du projet</h1>

<p>
Dans l'oasis de Lagueïla en <a href="http://fr.wikipedia.org/wiki/Mauritanie">Mauritanie</a>, il s'agit de financer la construction et l'équipement d'une école, d'un jardin pédagogique, du logement de l'instituteur et  d'un puits proche de l'école et des cases.
</p>


<h1>Les motifs</h1>

<p>
L'Oasis de Lagueïla compte une centaine d'habitants. Elle est située
au milieu des <a href="http://maps.google.fr/?ie=UTF8&amp;ll=20.390009,-12.397385&amp;spn=0.190512,0.265045&amp;t=h&amp;z=12&amp;om=1">dunes de Lemghalig</a> à 11 km au sud de <a
href="http://fr.wikipedia.org/wiki/Chinguetti">Chinguetti</a> dans
l'Adrar en Mauritanie.
</p>

<p>
Voici comment un habitant de l'oasis décrit la situation :
</p>

<blockquote>
<p>
La <acronym title="Région administrative">Wilaya</acronym> de l'Adrar
en général et la <acronym title="Département ou
district">Moughataa</acronym> de Chinguetti en particulier ont connu
ces dernières années une sécheresse sans précédent. Des problèmes se
posent au quotidien : l'eau, la santé, la scolarisation des enfants.
</p>
</blockquote>

<div class="image-gauche">
  <img src="village.jpg" alt="Un abreuvoir pour les chameaux"/>
</div>

<p>Malgré toutes les difficultés, les habitants de l'oasis restent
fidèles à leur lieu de vie. Ils veulent faire face aux difficultés et
trouver des solutions respectant leur histoire, leurs traditions et
leur mode de vie.
</p>

<p>Les associations <em>Waha Medrassa</em> et <em>Une oasis, une
école</em> ont uni leurs efforts pour créer une école assurant une
scolarité normale à tous les enfants et leur évitant de longs
déplacements à pied, à travers les dunes.
</p>

<h1>L'organisation du projet</h1>

<h2>En Mauritanie</h2>

<div class="image-droite">
  <img src="mohamed.jpg" alt="Mohamed Ould Barka"/>
</div>


<p>L'association <em>Waha Medrassa</em> est garante de la réalisation
et de la gestion du projet.</p>

<p>Ahmed Ould El Beyedh, chef du village,
représente les habitants de l'oasis au sein de l'association.
Mohamed Ould Barka, président de l'association, conçoit, organise
et conduit le projet en Mauritanie.</p>

<h2>En France</h2>

<div class="image-gauche">
  <img src="habitants.jpg" alt="Des enfants du village"/>
</div>

<p>L'association <em>Une oasis, une école</em> veut aider les
habitants de l'oasis à construire et équiper un bâtiment, édifié en
pierre, capable d'accueillir une quarantaine de garçons et filles du
primaire, à la rentrée 2007-2008.</p>

<p>Pour cela, elle récolte des fonds, les transfère, suit leur
utilisation et rend compte aux donateurs.</p>

<p>La récolte des fonds se fait par :</p>
<ul>
  <li>recherche de donateurs et de subventions</li>
  <li>participations à des manifestations associatives culturelles,
  brocantes</li>
  <li>vente de bijoux mauritaniens</li>
</ul>

