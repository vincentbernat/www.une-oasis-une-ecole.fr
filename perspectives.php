<h2>Coopérative avicole à Chinguetti</h2>

<p>
Dans le contexte actuel de crise alimentaire, l'achat de viande devient
de plus en plus difficile pour les familles. L'association <em>Taawniya désert</em> &ndash;
Coopérative du désert &ndash; s’est constituée pour travailler en partenariat avec
l’association <em>Une oasis, une école</em>,  à la construction d’un poulailler à
<a href="http://fr.wikipedia.org/wiki/Chinguetti">Chinguetti</a>. Celui-ci
devrait permettre la production d’œufs et de poules de chair vendus au
« prix le plus bas » afin de permettre aux populations les plus
isolées et les plus nécessiteuses de se procurer viande et œufs
nécessaires à leur équilibre alimentaire et d'assurer de meilleures
conditions de vie aux enfants de ces oasis.
</p>

<div class="image-droite">
  <img src="chinguetti.jpg" alt="Chinguetti"/>
</div>

<p>
En Mauritanie, Mohamed Ould Barka, président de l'association <em>Waha
Medrassa</em> et de l'association <em>Taawniya désert</em> conçoit, organise et conduit le
projet. <em>Taawniya désert</em> devra ensuite être en capacité :
<ul>
  <li>d'assumer les frais de fonctionnement (location du terrain, salaire du
gérant, achats de nourriture et de produits sanitaires, renouvellement du stock
de poules)</li>
  <li>de conserver un fonds de sécurité nécessaire à la pérennité du
    projet.
</li>
</ul>
</p>

<div class="image-gauche">
  <img src="chinguetti2.jpg" alt="Chinguetti"/>
</div>

<p>
Elle s'engage à redistribuer les bénéfices supplémentaires dans des
actions permettant l'aide au développement local, par exemple : aide alimentaire
directe, aide à la scolarisation, à la santé...
</p>

<p>En France, l'association <em>Une Oasis, une École</em> récolte des
fonds, les transfère, suit leur utilisation, rend compte et assurera
le fonctionnement pendant 6 mois.  La réalisation de ce projet
nécessite 25 000 €.
</p>

<h2>Partenariat avec les écoles</h2>

<p>Avec le collège de la Croix de Metz
de <a href="http://fr.wikipedia.org/wiki/Toul">Toul</a>, un travail
sur la différence se fera avec une ou plusieurs classes. Une récolte
de fonds et une bourse aux livres sont également envisagées. La
  première réunion a eu lieu le 24 février 2009.
</p>

<p>
Le partenariat avec les écoles primaires se poursuit avec notamment la
participation à la fête de la Com' : intervention de notre association
dans les écoles
de <a href="http://fr.wikipedia.org/wiki/Foug">Foug</a>.
</p>

<div class="image-droite">
  <img src="colliers.jpg" alt="Vente de colliers artisanaux"/>
</div>

<p>
Au cours de différentes manifestations telles que <em>Marché du Monde
 Solidaire</em>, nous présentons un diaporama sur la vie des nomades
 de
 l'<a href="http://fr.wikipedia.org/wiki/Adrar_(Mauritanie)">Adrar</a>
 et nous vendons des bijoux et de l'artisanat mauritaniens réalisés
 par des coopératives de femmes des petites oasis.</p>

<p>Le bénéfice est entièrement reversé et permet de continuer notre partenariat
scolaire et d'assurer le démarrage du projet 2009 : création d'une petite
coopérative <a href="http://fr.wikipedia.org/wiki/Aviculture">avicole</a>.</p>

