<h1>Rencontre de juillet 2007</h1>

<p>En juillet 2007, notre association a rencontré les habitants de
  l'oasis et les responsables de l'association <em>Waha Medrassa</em>.
</p>

<div class="image-droite">
  <img src="enfants-dessins.jpg" alt="Découverte des dessins"/>
</div>

<p>Au cours de cet échange, Mohamed Ould Barka présente aux enfants de
  Lagueïla les travaux réalisés par les enfants des écoles
  de <a href="http://fr.wikipedia.org/wiki/Chaudeney-sur-Moselle">Chaudeney</a>
  et de <a href="http://fr.wikipedia.org/wiki/Lay-Saint-Remy">Lay
  Saint Rémy</a>. Les petits mauritaniens sont émerveillés et émus
  qu'on ait fait des dessins pour eux en France et attendent avec
  impatience de futurs échanges.
</p>

