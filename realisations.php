<h1>Inauguration de l'école</h1>

<p>Le 12 mars 2008, notre association, représentée par Rolande Bernat,
  a été reçue par l'association <em>Waha Medrassa</em> à l'oasis de
  Laguïla.
</p>

<p>Les élèves, âgés de 5 à 13 ans, leur maître Mohamed Oudaa, le chef
  du village et le président Mohamed Ould Barka nous accueillaient
  dans leur belle école.
</p>

<div class="image-droite">
  <img src="classe-exterieur.jpg" alt="Les élèves devant l'école"/>
</div>

<p>
  Mots et chants de bienvenue, échanges de correspondance, de dessins,
  de posters avec les deux écoles du Toulois, cadeaux, distribution de
  petit matériel scolaire, de livres et beaucoup de photos. Autant de
  moments émouvants et riches d'espoir. Les contacts avec la
  population (distribution de vêtements, achats de bijoux artisanaux,
  discussions autour du thé) ont été chaleureux.
</p>

