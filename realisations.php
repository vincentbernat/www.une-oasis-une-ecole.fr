<h1>Construction de l'école</h1>

<p>Grâce au travail sur place des membres de <em>Waha Medrassa</em> et
des fonds récoltés par <em>Une oasis, une école</em> (6500 €), l'école
est construite et la matériel scolaire est acheté (1500 €).
</p>

<p>Le gouvernement mauritanien a nommé un
  enseignant.</p>

<div class="image-gauche">
  <img src="transport.jpg" alt="Transport à dos de chameaux"/>
</div>

<p>Tous les matériaux de construction ont été acheminés à dos d'ânes
  ou de chameaux. Il n'y a pas de piste...
</p>

<p>L'entretien de l'école sera réalisé par les habitants.</p>

<div class="image-droite">
